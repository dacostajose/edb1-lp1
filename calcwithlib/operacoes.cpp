#include <cmath>

int soma(int numero1, int numero2){
    return numero1+numero2;
}
int subtracao(int numero1, int numero2){
    return numero1-numero2;
}
int mutiplicacao(int numero1, int numero2){
    return numero1*numero2;
}
int divisao(int numero1, int numero2){
    return numero1/numero2;
}
int raiz_quadrada(int numero1){
    return sqrt(numero1);
}